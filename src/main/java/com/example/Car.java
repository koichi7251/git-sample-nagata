package com.example;

public class Car {
	private int speed;
	private String bodyColor;
	
	public final int getSpeed() {
		return speed;
	}
	public final void setSpeed(int speed) {
		this.speed = speed;
	}
	public final String getBodyColor() {
		return bodyColor;
	}
	public final void setBodyColor(String bodyColor) {
		this.bodyColor = bodyColor;
	}
}
